package LR1;

public class Example3 {
    public static void main(String[] args) {
        int a, b;
        a = 10;
        b = 20;
        if (a < b) {
            System.out.println("a < b");
        }
        a = a * 2;
        if (a == b) {
            System.out.println("a = b");
        }
        a = a * b;
        if (a > b) {
            System.out.println("a > b");
        }

    }

}

